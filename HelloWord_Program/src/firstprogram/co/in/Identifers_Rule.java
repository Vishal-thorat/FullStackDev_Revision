package firstprogram.co.in;

public class Identifers_Rule {

	public static void main(String[] args) {
		int rollNumber=10;
		int RollNumber=100;
		int rollnumer=1000;
		int ROLLNUMBER=100000;
		int NolLnumbER=1000000;
		
		System.out.println("rollNumber = " + rollNumber);
		System.out.println("RollNumber = " + RollNumber);
		System.out.println("rollnumer = " + rollnumer);
		System.out.println("ROLLNUMBER = " + ROLLNUMBER);
		System.out.println("NolLnumbER = " + NolLnumbER);
		
		/*
		 * Only allowed character in java [A-Z],[a-z],[0-9],underscore,$ 
		 * If we used other character it will give compile time error
		 * java lang is case sensitive,uppercase,lowercase identifiers are consider as different
		 * we can't use reserved keywords as identifers
		 * There is no limit for java identifers but not recommended to take more than 15 character.
		 * All predefined java classes and interface names we can use as identifers 
		 */

	}

}

